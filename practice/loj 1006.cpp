int md=10000007;
int dp[10005];
int a, b, c, d, e, f;
int fn( int n ) {
    if( n == 0 ) return a;
    if( n == 1 ) return b;
    if( n == 2 ) return c;
    if( n == 3 ) return d;
    if( n == 4 ) return e;
    if( n == 5 ) return f;
    if(dp[n]!=-1)return dp[n];
    return dp[n]=( fn(n-1)%md + fn(n-2)%md + fn(n-3)%md + fn(n-4)%md + fn(n-5)%md + fn(n-6)%md )%md;
}
int main() {
    int n, caseno = 0, cases;
    scanf("%d", &cases);
    while( cases-- ) {
        scanf("%d %d %d %d %d %d %d", &a, &b, &c, &d, &e, &f, &n);
        ms(dp,-1);
        printf("Case %d: %d\n", ++caseno, fn(n) % md);
    }
    return 0;
}
